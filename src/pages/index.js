import React from 'react';
import classnames from 'classnames';
import Layout from '@theme/Layout';
import Link from '@docusaurus/Link';
import useDocusaurusContext from '@docusaurus/useDocusaurusContext';
import useBaseUrl from '@docusaurus/useBaseUrl';
import styles from './styles.module.css';

const features = [
  {
    title: <Link to="/docs/getting-started">Your First Application</Link>,
    imageUrl: 'img/first-application.svg',
    description: (
      <>
        If you do not have a custom application ready for deployment, you
        can follow along with the sample application tutorial!
      </>
    ),
  },
  {
    title: <Link to="/docs/ready-to-deploy">Deployment</Link>,
    imageUrl: 'img/deployment.svg',
    description: (
      <>
        If you are ready to deploy your application, use our deployment docs
        to get it into the product.
      </>
    ),
  },
  {
    title: <Link to="/docs/best-security-practices">Security</Link>,
    imageUrl: 'img/sample-apps.svg',
    description: (
      <>
        Follow the Secure Deployment Practices documentation to ensure your
        application didn't introduce any potential vulnerabilities.
      </>
    ),
  },
];

function Feature({imageUrl, title, description}) {
  const imgUrl = useBaseUrl(imageUrl);
  return (
    <div className={classnames('col col--4', styles.feature)}>
      {imgUrl && (
        <div className="text--center">
          <img className={styles.featureImage} src={imgUrl} alt={title} />
        </div>
      )}
      <h3>{title}</h3>
      <p>{description}</p>
    </div>
  );
}

function Home() {
  const context = useDocusaurusContext();
  const {siteConfig = {}} = context;
  return (
    <Layout
      description="Description will go into a meta tag in <head />">
      <header className={classnames('hero hero--primary', styles.heroBanner)}>
        <div className="container">
          <h1 className="hero__title">{siteConfig.title}</h1>
          <p className="hero__subtitle">{siteConfig.tagline}</p>
          <div className={styles.buttons}>
            <Link
              className={classnames(
                'button button--outline button--secondary button--lg',
                styles.getStarted,
              )}
              to={useBaseUrl('docs/introduction')}>
              Learn More
            </Link>
          </div>
        </div>
      </header>
      <main>
        {features && features.length > 0 && (
          <section className={styles.features}>
            <div className="container">
              <div className="row">
                {features.map((props, idx) => (
                  <Feature key={idx} {...props} />
                ))}
              </div>
            </div>
          </section>
        )}
      </main>
    </Layout>
  );
}

export default Home;
